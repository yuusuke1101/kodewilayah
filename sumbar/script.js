// Update functions remain unchanged
function updateKecamatan() {
    const kabupatenSelect = document.getElementById('kabupaten');
    const kecamatanSelect = document.getElementById('kecamatan');
    
    kecamatanSelect.innerHTML = '<option value="">Pilih Kecamatan</option>';
    
    const selectedKabupaten = kabupatenSelect.value;
    const kecamatans = kecamatanData[selectedKabupaten];
    
    if (kecamatans) {
        kecamatans.forEach(kecamatan => {
            const option = document.createElement('option');
            option.value = kecamatan.kode;
            option.textContent = kecamatan.nama;
            kecamatanSelect.appendChild(option);
        });
    }
}

function updateKodeWilayah() {
    const kecamatanSelect = document.getElementById('kecamatan');
    
    const selectedKecamatan = kecamatanSelect.value;
    
    if (selectedKecamatan) {
        const kodeWilayah = selectedKecamatan;
        document.getElementById('kode-wilayah').innerText = kodeWilayah;
        document.getElementById('contoh-output').innerText = `{${kodeWilayah}RAHMAD}`;
        document.getElementById('output').style.display = 'block';
    } else {
        document.getElementById('output').style.display = 'none';
    }
}
